    /*
        Save a task by first checking:
        whether a project is selected or created,
        or task description is entered, 
        or the timer is already started.
    **/
    private void btn_saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_saveMouseClicked


        if( projectNames.getSelectedItem().toString().equals("None") ) {
            JOptionPane.showMessageDialog(null, "Please select a project first", "Can't Save" , JOptionPane.INFORMATION_MESSAGE);
        } else if( descriptionTextField.getText().equals("Task Description Here...") ) {
            JOptionPane.showMessageDialog(null, "Please enter task description before saving", "Can't Save" , JOptionPane.INFORMATION_MESSAGE);
        } else if ( minuteCounter == 0 && secondCounter == 0) {
            JOptionPane.showMessageDialog(null, "Please start the timer before saving", "Timer" , JOptionPane.INFORMATION_MESSAGE);

        } else {

            //Change mode to stop when a task is successfully saved
            if( mode ) {
                stopTimer();
                mode = false;
            }
            
            //Get current time to be recorded in task details
            DefaultTableModel model = (DefaultTableModel) dataTable.getModel();

            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate localDate = LocalDate.now();

            //Calculate total time taken and then reset those labels
            if( minuteCounter == 0 ) {
                totalHours = totalHours + 0.1;
                Object[] row = { descriptionTextField.getText(), dtf.format(localDate), startTime, endTime, "0." + Integer.toString(1) };
                model.addRow(row);
            } else {
                totalHours = totalHours + minuteCounter;
                Object[] row = { descriptionTextField.getText(), dtf.format(localDate), startTime, endTime, Integer.toString(minuteCounter) };
                model.addRow(row);
            }

            //Reset labels here
            totalHoursLbl.setText(Double.toString(totalHours));
            minuteCounter = 0;
            secondCounter = 0;

            minuteTimer.setText("0" + Integer.toString(minuteCounter));
            secondTimer.setText( ":" + Integer.toString(secondCounter));
            categoryLabel.setVisible(true);
            pieLabel.setVisible(true);

            startDurationLabel.setText("Start Time: --");
            descriptionTextField.setText("Task Description Here...");
            
            ImageIcon icon = new ImageIcon(getClass().getResource("/learn/OpacSave.png"));
            btn_save.setIcon(icon);
            
            JOptionPane.showMessageDialog(null, "Saved!", "" , JOptionPane.INFORMATION_MESSAGE);
            

        }

    }//GEN-LAST:event_btn_saveMouseClicked

