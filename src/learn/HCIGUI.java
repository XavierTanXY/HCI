    //Runs the timer using Timer class on another thread
    private void runTimer() {
        //Change icon to stop icon
        ImageIcon icon = new ImageIcon(getClass().getResource("/learn/Stop.png"));
        startButton.setIcon(icon);
        
        //Get current start time
        cal = Calendar.getInstance();
        startTime = sdf.format(cal.getTime());
        startDurationLabel.setText("Start Time: " + startTime);
             
        //New timer
        java.util.Timer timer = new java.util.Timer(); 
        
        //setting the counter to 10 sec
        secondCounter = 0; 
        TimerTask task = new TimerTask() {     
            
            public void run() {  
                //the timer lable to counter.
                secondTimer.setText( ":" + Integer.toString(secondCounter)); 
                secondCounter ++;
                
                // if it is 60 seconds, increments 1 to minute
                if( secondCounter == 61 ) {
                    minuteCounter++;
                    minuteTimer.setText("0" + Integer.toString(minuteCounter));
                    secondCounter = 0;
                }
                
                
                if (secondCounter == -1){
                    timer.cancel();                                       
                } else if(isIt){
                    timer.cancel();
                    isIt = false;
                }
            }
        };
        
        // =  timer.scheduleAtFixedRate(task, delay, period);
        timer.scheduleAtFixedRate(task, 1000, 1000); 
    }
    
